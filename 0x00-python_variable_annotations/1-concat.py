#!/usr/bin/env python3
"""
This module contains the concat function.
"""

def concat(str1: str, str2: str) -> str:
    """
    Takes two strings, str1 and str2, and returns their concatenated result.

    :param str1: First string.
    :param str2: Second string.
    :return: The concatenated string of str1 and str2.
    """
    return str1 + str2

